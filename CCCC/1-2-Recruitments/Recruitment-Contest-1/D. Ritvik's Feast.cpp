const int MOD = (int) 1e9 + 7;

int32_t main() {
	setup(); int tc; input(tc); while (tc--) {
		int n;
		input(n);

		vector<int> d(n);
		arrPut(d);

		int l = 0, r = n - 1;
		while (l < n and d[l] >= l) {
			l++;
		}
		l--;

		while (r >= 0 and d[r] >= n - r - 1) {
			r--;
		}
		r++;

		if (l >= r) {
			print("Yes");
		}
		else {
			print("No");
		}
	}
}
