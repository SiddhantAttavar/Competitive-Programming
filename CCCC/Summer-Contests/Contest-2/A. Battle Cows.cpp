int32_t main() {
	setup(); int tc; input(tc); while (tc--) {
		int n, k;
		input(n, k);

		k--;

		vector<int> a(n);
		arrput(a);

		int l = 0;
		while (l < n and a[l] < a[k]) {
			l++;
		}

		int r = l + 1;
		while (r < n and a[r] < a[k]) {
			r++;
		}

		if (r == n) {
			print(n - 1);
			continue;
		}

		if (l == k) {
			print(r - 1);
		}
		else {
			print(max(l - (l > 0), r - l - 1 + (l > 0)));
		}
	}
}
