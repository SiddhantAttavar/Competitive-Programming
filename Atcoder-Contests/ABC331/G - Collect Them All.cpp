const int MOD = 998244353;

int mod_pow(int a, int b) {
	int res = 1;
	while (b) {
		if (b & 1) {
			res = (res * a) % MOD;
		}

		a = (a * a) % MOD;
		b >>= 1;
	}
	return res;
}

int mod_div(int a, int b) {
	return (a * mod_pow(b, MOD - 2)) % MOD;
}

int solve(int n, int m, int x) {
	if (x == 0) {
		return 1;
	}

	return (solve(n, m, x - 1) + mod_div(m, x)) % MOD;
}

int32_t main() {
	setup();

	int n, m;
	input(n, m);

	vector<int> a(m);
	arrPut(a);
	int x = 0;
	for (int i : a) {
		x += !i;
	}

	print(solve(n, m, x));
}
