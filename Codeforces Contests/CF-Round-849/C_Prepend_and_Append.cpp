int32_t main() {
	setup(); int tc; input(tc); while (tc--) {
		int n;
		input(n);

		string s;
		input(s);
		int i = 0;
		while (i < n) {
			if (s[i] == s[n - i - 1]) {
				break;
			}
			i++;
		}

		if (i == n) {
			print(0);
		}
		else {
			print(n - 2 * i);
		}
	}
}
