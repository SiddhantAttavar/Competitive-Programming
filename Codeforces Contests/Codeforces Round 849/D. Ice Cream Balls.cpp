int32_t main() {
	setup(); int tc; input(tc); while (tc--) {
		int n;
		input(n);

		int x = (int) ceil((sqrt(1 + 8 * n) + 1) / 2) - 1;
		if (x * (x + 1) / 2 == n) {
			print(x + 1);
			continue;
		}

		int y = x * (x - 1) / 2;
		// print(x, y);

		int res = x + n - y;
		print(max(2ll, res));
	}
}
