#define vi vector<int>
struct UF {
	vi e;
	UF(int n) : e(n, -1) {}
	bool sameSet(int a, int b) { return find(a) == find(b); }
	int size(int x) { return -e[find(x)]; }
	int find(int x) { return e[x] < 0 ? x : e[x] = find(e[x]); }
	bool join(int a, int b) {
		a = find(a), b = find(b);
		if (a == b) return false;
		if (e[a] > e[b]) swap(a, b);
		e[a] += e[b]; e[b] = a;
		return true;
	}
};

int32_t main() {
	setup();

	int n, q;
	input(n, q);

	UF d(n);
	while (q--) {
		string o;
		int u, v;
		input(o, u, v);

		if (o == "union") {
			d.join(u - 1, v - 1);
		}
		else if (d.sameSet(u - 1, v - 1)) {
			print("YES");
		}
		else {
			print("NO");
		}
	}
}
